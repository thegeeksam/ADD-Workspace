//2. while Loop
var i = 1;
while (i < 10) {
    console.log("The number is " + i);
    i++;
}
console.log("Executing a statement out of while loop");

// //3. do...while Loop
let j = 1;
do {
    console.log("The number is " + j);
    j++;
} while(j < 1);
console.log("Executing a statement out of do..while loop");