//--> Assignment Operator <--//

// Assignment Operator is used to assign values to variables.

let num = 7;

console.log(num += 5); // num = num + 5 
console.log(num -= 3); // num = num - 3 
console.log(num *= 2); // num = num * 2 
console.log(num /= 9); // num = num / 9
console.log(num %= 3); // num = num % 3 
console.log(num **= 3); // num = num ** 3 