//-->Ternary Operator <--//

/*
Ternary Operator is the only JavaScript 
operator that takes 3 operands.

This operator is frequently used as a 
shorthand for the if-else statement.

Syntax: 
condition ? expressionIfTrue : expressionIfFalse;

*/

let age = 18;
let canVote = (age >= 18) ? "Yes" : "No";
console.log(canVote); 
