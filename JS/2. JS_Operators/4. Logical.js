//--> Logical Operators <--//

/*
Logical operators perform logical 
operations and return a boolean value, 
either true or false. 
*/

let x = 5, y = 3;
console.log((x < 6) && (y > 5)); // Logical AND 
console.log((x < 6) || (y > 5)); // Logical OR
console.log((x != 6)); // Logical NOT 
